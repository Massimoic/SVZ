Scenario::Scenario() {
    floor = LoaderManager::LoadTexture("/Users/andreadiaz/projects/GAME/personajes/floor-test2.png");
    
    
    LoadScenario(place);
    
    src.x = src.y = 0;
    src.w = dst.w = 480;
    src.h = dst.h =  680;
    dst.x = dst.y = 0;
    
    
}

void Scenario::LoadScenario(int map[21][25]) {
    for (int row = 0; row < 21; row++){
        for (int column = 0; column < 25 ; column ++){
            map[row][column] = place[row][column];
        }
    }
}

void Scenario::DrawScenario() {
    int type = 0;
    
    for (int row = 0; row < 21; row++){
        for (int column = 0; column < 25 ; column ++){
            type = map[row][column];
            
            dst.x = column * 63;
            dst.y = row * 90;
            
            switch (type) {
                case (1):
                    LoaderManager::Draw(floor, src, dst);
                    
                    break;
                    
                default:
                    break;
            }
        }
    }
}
